
public class NewAgent extends Agent{
	public NewAgent(){
		super();
	}
	public NewAgent(Lever lever,Dial dial,Brush brush){
		super(lever,dial,brush);
	}
	public void doSpeed()
	{
		int speed = 0;
		switch(getLever())
		{
		case 1:speed = 0;break;
		case 2:
			switch(getDial())
			{
			case 1: speed = 4;break;
			case 2: speed = 6;break;
			case 3: speed = 12;break;
			case 4: speed = 18;break;
			case 5: speed =24;break;
			}
			break;
		case 3: speed = 30;break;
		case 4: speed = 60;break;
		case 5: speed = 120;break;
		}
		brush.setSpeed(speed);
	}
	
	public void show()
	{
		String slever = "";
		switch(lever.getLever())
		{
		case 1: slever = "停止";break;
		case 2: slever = "间歇";break;
		case 3: slever = "低速";break;
		case 4: slever = "高速";break;
		case 5: slever = "超高速";break;
		}
		
		System.out.println("控制杆的档位为：" + slever );
		System.out.println("刻度盘的刻度为：" + dial.getDial());
		System.out.println("雨刷的速度为：" + brush.getSpeed());
	}

}
